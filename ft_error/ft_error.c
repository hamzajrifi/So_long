#include "../header.h"

void	ft_error(char *str)
{
	int		i;

	i = ft_strlen(str);
	write(2, str, i);
	exit(1);
}

/* --------------- */
int	ft_bye(char *ptr)
{
	write(1, ptr, ft_strlen(ptr));
	exit(0);
}
